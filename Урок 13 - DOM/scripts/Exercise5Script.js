function addElement() {
  const para = document.createElement("p");
  para.innerHTML = "Параграф";
  document.body.appendChild(para);
}
function addColorElement() {
  const para = document.createElement("p");
  para.innerHTML = "Параграф";
  para.style.color = "blue";
  document.body.appendChild(para);
}
